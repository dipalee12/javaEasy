package b.operators;

public class ComperitionOperators {

public static void main(String[] args) {
		
		int a=3;
		int b=2;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		

	}

}
